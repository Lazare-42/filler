#include "../includes/libft.h"

void	ft_mystriter(char **s, char (*f)(char))
{
	char	*tmp;

	tmp = *s;
	if (*s && f)
	{
		while (*tmp)
		{
			*tmp = f(*tmp);
			tmp++;
		}
	}
}
