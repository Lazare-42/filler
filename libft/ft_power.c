#include "libft.h"

int	ft_power(int n, int pow)
{
	int		i;
	int		nref;

	nref = n;
	i = 1;
	if (pow == 0)
		return (1);
	while (i < pow)
	{
		n *= nref;
		i++;
	}
	return (n);
}
