#include "libft.h"

int		ft_n_len(long long n)
{
	int		l;

	l = 0;
	n = ft_abs(n);
	if (n == 0)
		return (1);
	while (n > 0)
	{
		l++;
		n /= 10;
	}
	return (l);
}
