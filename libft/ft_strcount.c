#include "libft.h"

int	ft_strcount(char *s, char c)
{
	int	i;
	int	n;

	i = 0;
	n = 0;
	while (s[i])
	{
		if (s[i] != c)
		{
			n++;
			while (s[i] != c && s[i])
				i++;
			i--;
		}
		i++;
	}
	return (n);
}
